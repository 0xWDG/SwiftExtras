#if canImport(SwiftUI)
import SwiftUI

/// Optional binding
/// - Parameters:
///   - lhs: Optional binding value
///   - rhs: Value if the optional binding value is nil
/// - Returns: Binding
public func ?? <T>(lhs: Binding<T?>, rhs: T) -> Binding<T> {
    Binding(
        get: { lhs.wrappedValue ?? rhs },
        set: { lhs.wrappedValue = $0 }
    )
}
#endif
